# Sample: Python venv Explanation
"""
Python venv (virtual environment) is used to create isolated environments for your projects.
This allows you to manage dependencies separately for each project.

How to create and use venv:

1. Create a virtual environment:
   python -m venv .venv

2. Activate the environment:
   # Windows
   .venv\Scripts\activate
   # Linux/Mac
   source .venv/bin/activate

3. Install packages:
   pip install package_name

4. Deactivate:
   deactivate

Example code:
import sys
print("Current Python executable:", sys.executable)
"""
