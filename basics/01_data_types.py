# Sample: Python Data Types

# Numbers
x = 10
pi = 3.14

# Strings
name = "Alice"

# Lists
fruits = ["apple", "banana", "cherry"]

# Tuples
coordinates = (10, 20)

# Dictionaries
person = {"name": "Bob", "age": 25}

# Sets
unique_numbers = {1, 2, 3, 3}

# Type conversion
num_str = str(x)
str_num = int("42")

print("Data Types Demo:", x, pi, name, fruits, coordinates, person, unique_numbers, num_str, str_num)
